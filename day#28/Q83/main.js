"use strict";
// Question 83: Converting to Upper and Lower Case: Create a function that takes a string, 
// converts it to uppercase, then to lowercase, and logs both.
function converting(value1) {
    let upper = value1.toUpperCase();
    let lower = value1.toLowerCase();
    console.log(`Upercase: ${upper}, Lowercase: ${lower}`);
}
;
converting("Typescript is a popular language.");
