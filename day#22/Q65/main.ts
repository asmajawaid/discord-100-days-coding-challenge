// Q65 - Determine the Remainder: Make a function that gets two numbers and shows the 
// leftover from dividing them using the % sign. For example, remainder(5, 2) should 
// give 1.


function leftoverNo(num1:number,num2:number):number {
      // Gives back the leftover of num1 divided by num2
    return num1 % num2
};

 console.log(leftoverNo(5,2));
 // This tells us the leftover, which is 1 here.