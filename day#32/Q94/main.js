// Question 94: Use the .map() method to create a new array that contains the length of
// each word from an array of words.
let newArrwords = ["apple", "banana", "mango", "orange"];
let Array0 = newArrwords.map((newArrwords) => newArrwords.length);
// Each number in the new array represents the length of the corresponding word in the original array.
console.log(Array0);
