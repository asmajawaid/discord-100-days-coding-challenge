// Question 73: Assigning and Updating Variables: Create a function where you declare a 
// variable using let and assign an initial value. Then, update its value within the same 
// function and log both the initial and updated values.

// This function shows how to assign and update variable values
function assignUpdateVariable() {
    let indoorGames ="Hopscotch"; // Initially assigns the value 
    console.log("initialized value:", indoorGames);
    indoorGames ="Scavenger Hunt"; // update the value
    console.log("updated value:", indoorGames);
};
assignUpdateVariable(); //invoking function
// We assign a value to a variable and then update it, showing how values can change in funcction.